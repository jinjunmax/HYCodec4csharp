        public 	int FindNumberByString(string str)
        {            
            for (int i = 0; i != szStandString.Count; i++)
            {
                if (szStandString[i] == str)
                    return i;
            }

            return -1;
    
        }   // end of public int FindNumberByString()

        public int InsertString(string str)
        {
            int iIndex = FindNumberByString(str);
            if (iIndex == -1)
            {
                szStandString.Add(str);
                return szStandString.Count - 1;
            }

            return iIndex;

        }   // end of public int InsertString()

